async function seedCountries() {
  try {
    console.log('Checking existing countries...')
    const existingCount = await db.country.count()
    
    if (existingCount > 0) {
      console.log(`Found ${existingCount} existing countries in the database.`)
      console.log('Updating existing countries and adding new ones...')
    } else {
      console.log('No existing countries found. Creating new countries...')
    }
    
    console.log(`Seeding ${COUNTRIES.length} countries...`)
    
    // Use upsert for each country to handle existing records gracefully
    for (const country of COUNTRIES) {
      const requiresVat = EU_COUNTRIES.includes(country.code)
      
      await db.country.upsert({
        where: { code: country.code },
        update: {
          name: country.name,
          requiresVat: requiresVat,
        },
        create: {
          code: country.code,
          name: country.name,
          requiresVat: requiresVat,
        },
      })
    }
    
    console.log(`✅ Successfully seeded ${COUNTRIES.length} countries!`)
    console.log(`   ${EU_COUNTRIES.length} EU countries require VAT`)
    
    // Close database connection
    await db.$disconnect()
    process.exit(0)
  } catch (error: any) {
    console.error('❌ Error seeding countries:', error.message)
    console.error(error)
    await db.$disconnect()
    process.exit(1)
  }
}

seedCountries()

